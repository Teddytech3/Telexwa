// ─── convert a single character ──────────────────────────────

function convertChar(char, font) {
  const lIdx = NORMAL_LOWER.indexOf(char);
  if (lIdx !== -1) { const arr = [...font.lower]; return arr[lIdx] || char; }
  const uIdx = NORMAL_UPPER.indexOf(char);
  if (uIdx !== -1) { const arr = [...font.upper]; return arr[uIdx] || char; }
  const dIdx = NORMAL_DIGITS.indexOf(char);
  if (dIdx !== -1) { const arr = [...font.digits]; return arr[dIdx] || char; }
  return char;
}

// ─── apply font to a text string ─────────────────────────────
//  getSetting must be the session-scoped getSetting from command.js
//  (already namespaced to botNumber — no extra arg needed)

function applyFont(text, getSetting) {
  const fontName = getSetting('font', 'normal');
  if (fontName === 'normal' || !FONTS[fontName]) return text;
  const font = FONTS[fontName];

  // Preserve: URLs, @mentions, WhatsApp bold (*text*), italic (_text_), code (```text```)
  const preserveRegex = /https?:\/\/[^\s]+|@\d[\d\s]*|```[\s\S]*?```|\*[^*]+\*|_[^_]+_/g;

  let result = '';
  let lastIndex = 0;
  let match;

  while ((match = preserveRegex.exec(text)) !== null) {
    const before = text.slice(lastIndex, match.index);
    for (const ch of before) result += convertChar(ch, font);
    result += match[0];
    lastIndex = match.index + match[0].length;
  }

  const remaining = text.slice(lastIndex);
  for (const ch of remaining) result += convertChar(ch, font);

  return result;
}

function getFontList() {
  return Object.keys(FONTS);
}

module.exports = { applyFont, getFontList, FONTS };
